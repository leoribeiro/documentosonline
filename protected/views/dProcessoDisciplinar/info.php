<h3>REGIME DISCIPLINAR DISCENTE</h3>

<p>Capítulo I - Do Corpo Discente: Direitos e Deveres</p>

<p>Art.1º - O corpo discente do CEFET-MG é constituído de alunos regularmente matriculados nos cursos oferecidos pelo CEFET-MG, conforme seu Regimento Geral.</p>

<p>Art.2º - São direitos dos alunos:
<br />•	rematrícula;
<br />•	promoção para o período seguinte, desde que cumpridas as formalidades legais para aprovação;
<br />•	revisão de prova, desde que solicitada, à instância competente, dentro do prazo previsto;
<br />•	participação em órgãos colegiados, comissões instituídas para tratarem de matéria de ensino, pesquisa, extensão e outros assuntos concernentes à Instituição;
<br />•	utilização dos benefícios suplementares da Instituição;
<br />•	condições adequadas aos estudos, lazer e sua permanência na escola;
<br />•	ser tratado com respeito e dignidade por colegas, servidores da instituição e outros;
<br />•	recorrer das medidas punitivas, às instâncias escolares superiores;
<br />•	organização e participação em entidades estudantis;
<br />•	obtenção do material didático-escolar, produzido pelo CEFET-MG, a preços que atendem às necessidades da clientela do mesmo.</p>

<p>Art.3º - São deveres dos alunos:
<br />•	zelar pelo patrimônio da Instituição;
<br />•	respeitar os colegas, os servidores da Casa e outros;
<br />•	ressarcir os prejuízos causados aos bens patrimoniais da Instituição e de outros;
<br />•	respeitar os prazos, as normas e as regras gerais emanadas dos ordenamentos do CEFET-MG;
<br />•	comparecer ou apresentar-se, com pontualidade, às atividades e trabalhos escolares, em condições adequadas às situações;
<br />•	manter atitudes compatíveis com a moralidade ou a dignidade da vida acadêmica.</p>

<br /><br />
<p>Capítulo II - Do Regime Disciplinar</p>

<p>Art.4º - A ordem disciplinar no CEFET-MG deverá ser mantida com a cooperação ativa dos diversos segmentos da comunidade como condição indispensável ao êxito dos trabalhos</p>

<p>Art.5º - A inobservância das normas previstas pelo Regime Disciplinar desta Instituição implicará as seguintes sanções:
<br />•	Advertência;
<br />•	Repreensão;
<br />•	Suspensão das atividades escolares;
<br />•	Desligamento.</p>

<p>Art.6º - Na aplicação das sanções escolares será considerada a gravidade de infração, à vista dos seguintes elementos:
<br />•	Primariedade do infrator;
<br />•	Dolo ou culpa;
<br />•	Natureza da ofensa;
<br />•	Valor do bem moral, cultural ou material atingido;
<br />•	Circunstâncias em que ocorreu o fato.</p>

<p>Art.7º - As sanções previstas no Artigo anterior, serão aplicadas nos seguintes casos:
<br />I. Advertência:
<br />a) por desrespeito a qualquer autoridade do CEFET-MG, a qualquer membro dos Corpos Docente, Discente, Técnico-Administrativo e outros;
<br />b) por perturbação da ordem nos recintos do CEFET-MG.
<br /><br />
II. Repreensão:
<br />a) por reincidência em qualquer das faltas das alíneas do item anterior;
<br />b) por praticar atos incompatíveis com a moralidade ou dignidade da vida acadêmica;
<br />c) por convocação e realização de reuniões do Corpo Discente de Centro sem autorização prévia da(s) autoridade(s) responsável(s) pelo local da reunião.</p>

<p>III. Suspensão das atividades escolares:
<br />a) por reincidência em qualquer das faltas das alíneas do item II;
<br />b) por improbidade comprovada na execução de trabalhos escolares;
<br />c) por ofensa física ou moral a qualquer membro dos Corpos Discente, Docente, Técnico-Administrativo ou outros;
<br />d) por dano material causado ao patrimônio da Instituição ou particular.</p>

<p>IV. Desligamento
<br />a) por reincidência em qualquer das faltas das alíneas III;
<br />b) dolo comprovado de ato incompatível com a moralidade e a dignidade da vida acadêmica.</p>

<p>Art.8º - A suspensão, por qualquer que seja sua duração, impedirá o exercício de representação em qualquer colegiado ou comissão, durante o seu impedimento.</p>

<p>Parágrafo único - Na aplicação de suspensão das atividades escolares, a autoridade deverá fixar, expressamente, o período de afastamento, que não deverá exceder a 15% (quinze por cento) do período letivo.</p>

<p>Art.9° - São competentes para aplicar as sanções previstas no artigo 7º:
<br />•	os professores, os coordenadores, os Chefes de Departamentos Acadêmicos, de Setor de Disciplina Escolar, dos Departamentos de Ensino Superior e de 2º Grau, das Unidades, quando se tratar de advertência;
<br />•	o Chefe do Setor de Disciplina Escolar, dos Departamentos de Ensino Superior e de 2º Grau, das UNEDs, quando se tratar de repreensão;
<br />•	o Chefe do Setor de Disciplina Escolar, dos Departamentos de Ensino Superior e de 2º Grau, das UNEDs, quando se tratar de suspensão das atividades escolares;
<br />•	Diretor-Geral, quando se tratar de desligamento.</p>

<p>Parágrafo 1° - A advertência será aplicada oralmente, na presença de, pelo menos, uma testemunha, e comunicada, por escrito, à autoridade competente.</p>

<p>Parágrafo 2º - A repreensão será, sempre, por escrito.</p>

<p>Parágrafo 3º - A suspensão das atividades escolares, por até 03 (três) dias, será por ato do Chefe do Setor de Disciplina escolar e por até 05 (cinco) dias, por ato dos Chefes dos Departamentos de Ensino Superior, de 2º Grau e das Unidades.</p>

<p>Parágrafo 4° - A suspensão das atividades escolares por um período superior a 05 (cinco) dias, será da competência do Diretor de Ensino.</p>

<p>Parágrafo 5º - O desligamento será precedido de processo disciplinar, realizado por Comissão instituída pelo Diretor-Geral.</p>

<p>Parágrafo 6º - Será assegurado ao discente amplo direito de defesa.</p>

<p>Parágrafo 7º - O inquérito será instaurado mediante Portaria do Diretor-Geral, baixada dentro de 05 (cinco) dias do conhecimento do fato, concluído no prazo de 30 (trinta) dias, contados da data da Portaria. O prazo do inquérito poderá ser prorrogado, uma única vez, por igual período.</p>

<p>Parágrafo 8º - Após a apuração dos fatos, ouvidas as testemunhas e colhendo as provas que julgar necessárias, a Comissão dará vista do processo ao acusado, ou a seu procurador legalmente constituído, para, no prazo de 10 (dez) dias, apresentar sua defesa escrita.</p>

<p>Parágrafo 9º - Em caso de arrolar testemunhas, as mesmas serão ouvidas no prazo de 03 (três) dias, após a notificação.</p>

<p>Parágrafo 10º - A Comissão de Inquérito, após a instrução, enviará relatório ao Diretor-Geral, para decisão.</p>

<p>Parágrafo 11º - No caso de parecer da Comissão pelo desligamento serão remetidos os autos ao Conselho Diretor para decisão final.</p>

<p>Parágrafo 12º - Quando a infração estiver capitulada na Lei Penal, será remetido à autoridade competente, ficando traslado na repartição.</p>

