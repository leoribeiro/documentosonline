<p>Capítulo III - Dos recursos</p>

<p>Art.10º - Caberá pedido de reconsideração às autoridades ou órgãos desta Instituição, pelas suas decisões, ou recurso à instância imediatamente superior.</p>

<p>Parágrafo Único - O pedido de reconsideração suspende o prazo para a interposição de recurso e deverá ser apresentado 48 (quarenta e oito) horas após ciência do fato ou ato.</p>

<p>Art.11º - O recurso não terá efeito suspensivo e deverá ser interposto perante a autoridade ou órgão recorrido, dentro de 15 (quinze) dias, contados da data de conhecimento do ato recorrido.</p>

<p>Art.12º - O julgamento de qualquer recurso, a nível desta Instituição, terá o prazo de 25 (vinte e cinco) dias e o processo, devidamente julgado, será devolvido à autoridade ou órgão recorrido, para o cumprimento da decisão proferida.
Capítulo IV - Das disposições gerais</p>

<p>Art.13º - O registro da sanção será feito pelas Seções de registro escolar de 3º e 2º Graus, respectivamente, e não constará do histórico escolar do aluno.
Parágrafo Único - Será cancelado o registro das sanções previstas nos itens I e II do artigo 5º, se, no prazo de 01 (um) ano de aplicação, o discente não incorrer em reincidência.</p>

<p>Art.14º - Em caso de dano material ao patrimônio da Instituição e outros, além da sanção disciplinar aplicável, o infrator estará obrigado ao ressarcimento.</p>

<p>Parágrafo Único - Os prejuízos materiais serão apurados pelos órgãos competentes, conforme os bens avaliados, ouvida a Comissão de Avaliação.</p>

<p>Art.15º - Os casos omissos serão resolvidos pelo Diretor-Geral, ouvido o Conselho Diretor.</p>

<p>Art.16º - Este regulamento entra em vigor na data de sua aprovação pelo Conselho Diretor, fazendo parte integrante do Centro, revogadas todas as disposições em contrário.</p>
